import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '../..');
const source = readFileSync(path.join(repoRoot, 'findings.html'), 'utf8');

const mainMatch = source.match(/<main\b[^>]*id="main-content"[^>]*>([\s\S]*?)<\/main>/i);
if (!mainMatch) throw new Error('no main found');

let body = mainMatch[1];

const heroOpenRe = /<section class="hero"[^>]*>/i;
const heroOpen = body.match(heroOpenRe);
if (!heroOpen) throw new Error('no hero open found');
const heroOpenIdx = body.indexOf(heroOpen[0]);
let cursor = heroOpenIdx + heroOpen[0].length;
let depth = 1;
while (depth > 0 && cursor < body.length) {
  const open = body.slice(cursor).search(/<section\b/i);
  const close = body.slice(cursor).search(/<\/section>/i);
  if (close === -1) throw new Error('unbalanced section');
  if (open !== -1 && open < close) { depth++; cursor += open + 8; }
  else { depth--; cursor += close + 10; }
}
const heroEnd = cursor;
let afterHero = body.slice(heroEnd).trimStart();

// Legacy findings.html (lines 303-310) has a malformed structural
// pattern: the Acknowledgements block starts with a raw
// `<div class="container">` immediately after the citation-box
// section closes, and ends with an orphan `</section>` that has no
// matching opening tag. Browsers tolerate it under lenient HTML
// parsing; JSX requires balanced tags. Insert the missing
// `<section class="content-section">` open before the orphan
// container so the structure balances. This is a structural fix
// for malformed legacy HTML, not a content change — the visible
// text is identical and parity:text confirms.
afterHero = afterHero.replace(
  /(<\/section>\s*)(<div class="container">\s*<h2>Acknowledgements<\/h2>)/,
  '$1<section class="content-section">\n$2'
);

function cssToJsxStyle(decl) {
  const props = decl.split(';').map((s) => s.trim()).filter(Boolean);
  const entries = props.map((p) => {
    const colon = p.indexOf(':');
    const key = p.slice(0, colon).trim();
    const val = p.slice(colon + 1).trim();
    const camelKey = key.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
    return `${camelKey}: "${val}"`;
  });
  return `style={{ ${entries.join(', ')} }}`;
}

let jsx = afterHero
  .replace(/\bclass=/g, 'className=')
  .replace(/\bfor=/g, 'htmlFor=')
  .replace(/<br>/gi, '<br />')
  .replace(/<hr>/gi, '<hr />')
  .replace(/<input\b([^>]*)>/gi, '<input$1 />')
  .replace(/<img\b([^>]*?)(\s*\/)?>/gi, (_m, attrs) => {
    const cleaned = attrs.replace(/\s*\/?\s*$/, '');
    return `<img${cleaned} />`;
  })
  .replace(/<source\b([^>]*?)(\s*\/)?>/gi, (_m, attrs) => {
    const cleaned = attrs.replace(/\s*\/?\s*$/, '');
    return `<source${cleaned} />`;
  })
  .replace(/\bsrcset=/g, 'srcSet=')
  .replace(/\bstyle="([^"]+)"/g, (_match, decl) => cssToJsxStyle(decl));

const out = `/**
 * Findings index page (\`/findings\`) — eleventh page port.
 *
 * The page is wrapped by \`<EnhancedFindingsShell>\` (the same shell
 * the catch-all wraps around findings routes via the
 * \`enhanced-findings\` renderMode). The shell adds a sidebar/topbar
 * chapter navigator and a reader-panel overlay. Wrapping is done
 * in the route file (\`app/findings/page.tsx\`), not here.
 *
 * Body generated by \`scripts/port-findings-index.mjs\`. The script
 * applies these mechanical swaps:
 *
 * - \`class=\` -> \`className=\`
 * - \`for=\` -> \`htmlFor=\`
 * - \`<br>\` / \`<hr>\` / \`<input ...>\` / \`<source ...>\` ->
 *   self-closed JSX void forms
 * - \`srcset=\` -> \`srcSet=\` (React's prop name)
 * - inline \`style="kebab-case: val"\` -> JSX \`style={{ camel: "val" }}\`
 *
 * One structural fix: the legacy \`findings.html\` lines 303-310 are
 * malformed — the Acknowledgements block starts with a raw
 * \`<div class="container">\` and ends with an orphan \`</section>\`
 * that has no matching open. Browsers parse leniently, but JSX
 * requires balanced tags. The script inserts the missing
 * \`<section class="content-section">\` open. Visible text is
 * unchanged; \`parity:text\` confirms.
 */
export function FindingsIndexPage() {
  return (
    <main id="main-content">
      <section
        className="hero"
        style={{
          backgroundImage: "url('findings/Figures/0_Cover.jpg')",
          backgroundSize: "cover",
          backgroundPosition: "center",
          position: "relative"
        }}
      >
        <div className="hero-overlay"></div>
        <div className="container">
          <div className="hero-content">
            <h1 className="hero-title">Heaviness in Metal Music Production: A Practical Guide</h1>
            <p className="hero-text"><strong><em>Primarily written for metal producers, mix engineers, and advanced home recordists working in heavy music.</em></strong><br />This is where our research becomes your practical toolkit. Drawing on the 'Heaviness in Metal Music Production' (HiMMP) project, it distils our findings into an applied guide for contemporary metal production. You can read it on its own or alongside the project’s full dataset, videos, and publications at <a href="https://www.himmp.net">www.himmp.net</a>. Go hands-on with the core philosophies and technical analyses from our study; audio examples are provided throughout.</p>
          </div>
        </div>
      </section>
${jsx.trimEnd()}
    </main>
  );
}
`;

writeFileSync(path.join(repoRoot, 'nextjs-site/src/site/components/pages/FindingsIndexPage.tsx'), out);
console.log('wrote FindingsIndexPage.tsx (' + out.split('\n').length + ' lines)');
